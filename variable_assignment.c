int main() {
  // = is assignment operator

  // variable declaration and assignment
  int number1 = 7;
  // variable assignment
  number1 = 14;

  // only variable declaration
  int number2;
  number2 = 21;

  // variable assignment
  number2 = 5 + 4;
  number2 = number1 + number2 + 5 * 10;

  return 0;
}